class MissingPattern(Exception): pass

class InvalidMatch(Exception): pass

class Renderer:
    def __init__(self,
                 pre_processors=None,
                 patterns=None,
                 transforms=None,
                 descent_rules=None,
                 post_processors=None):
        self._pre_processors = pre_processors or []
        self._patterns = patterns or {}
        self._transforms = transforms or {}
        self._descent_rules = descent_rules or {}
        self._post_processors = post_processors or []
        self._descent_regexes = {}
        self.compile()

    def compile(self):
        self._descent_regexes.clear()
        for name, descendees in self._descent_rules.items():
            if descendees: self._descent_regexes[name] = re.compile(
                '|'.join(self._patterns[descendee_name] for descendee_name in descendees),
                re.DOTALL | re.MULTILINE
            )

    def parse(self, text):
        for p in self._pre_processors: text = p(text)
        text = self._parse(text)
        for p in self._post_processors: text = p(text)
        return text

    def _parse(self, text, rule=None):
        output = ''

        while text:
            match = self._descent_regexes[rule].search(text)
            if not match: break
            try:
                output += text[:match.start()] + self._transforms[match.lastgroup](match)
                text = text[match.end():]
            except InvalidMatch:
                output += text[0]
                text = text[1:]

        return output + text

